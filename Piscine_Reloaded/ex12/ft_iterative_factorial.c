int		ft_iterative_factorial(int nb)
{
	int i;
	int n;

	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	if (nb > 12)
		return (0);
	n = 1;
	i = 1;
	while (i < nb)
	{
		n = n * (i + 1);
		i++;
	}
	return (n);
}
